namespace ACAT.Extensions.Hawking.Actuators.Camera
{
    /// <summary>
    /// Settings for the Camera Actuator
    /// </summary>
    [Serializable]
    public class Settings : PreferencesBase
    {
        /// <summary>
        /// Name of the settings file
        /// </summary>
        [NonSerialized]
        public static String SettingsFilePath;

        /// <summary>
        /// Name of the vision app window.  Used to find the window and hide it
        /// </summary>
        public String ACATVisionWindowName = "ACAT Vision";

        /// <summary>
        /// IP Address Address of the vision software
        /// </summary>
        public String SocketClientConnectToAddress = "127.0.0.1";

        /// <summary>
        /// Port to connect to
        /// </summary>
        public int SocketClientConnectToPort = 27015;

        /// <summary>
        /// Loads the settings from the settings file
        /// </summary>
        /// <returns>true on success</returns>
        public static Settings Load()
        {
            Settings retVal = PreferencesBase.Load<Settings>(SettingsFilePath);
            Save(retVal, SettingsFilePath);
            return retVal;
        }

        /// <summary>
        /// Saves settings
        /// </summary>
        /// <returns>true on success</returns>
        public override bool Save()
        {
            return Save<Settings>(this, SettingsFilePath);
        }
    }
}